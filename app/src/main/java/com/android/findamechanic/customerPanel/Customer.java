package com.android.findamechanic.customerPanel;

public class Customer {

    private String street, password, confirmPassword, province, suburb, email, name, mobile;

    public Customer() {
    }

    public Customer(String street, String password, String confirmPassword, String province, String suburb, String email, String name, String mobile) {
        this.street = street;
        this.password = password;
        this.confirmPassword = confirmPassword;
        this.province = province;
        this.suburb = suburb;
        this.email = email;
        this.name = name;
        this.mobile = mobile;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getConfirmPassword() {
        return confirmPassword;
    }

    public void setConfirmPassword(String confirmPassword) {
        this.confirmPassword = confirmPassword;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getSuburb() {
        return suburb;
    }

    public void setSuburb(String suburb) {
        this.suburb = suburb;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
}
